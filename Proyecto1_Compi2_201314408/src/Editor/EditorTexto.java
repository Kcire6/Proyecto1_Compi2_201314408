    public void Parsear(String entrada)throws ParseException{
        InputStream stream = new ByteArrayInputStream(entrada.getBytes(StandardCharsets.UTF_8));
         
        if(parser == null){
             parser = new Calc1i(stream);
         }
         else{
             Calc1i.ReInit(stream);
         }
        try { switch (Calc1i.one_line()){//call to grammar start symbol
              case -1: System.out.println("Error");
              case 0: break;
              case 1:  // result is stored on top of stack
                int x = ((Integer) Calc1i.argStack.pop()).intValue();
                System.out.println("Total = " + x);
                break;
          }
        } catch (ParseException x) {
        System.out.println("Exiting."); throw x;
        }
    }
