    private void AbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AbrirActionPerformed
        JFileChooser jFileChooser = new JFileChooser();
        StringBuffer buffer;
        buffer = new StringBuffer();
        int result= jFileChooser.showOpenDialog(this);
        if(result==JFileChooser.APPROVE_OPTION)
        {
        try {
            FileReader reader;
            File file=jFileChooser.getSelectedFile();
            String nombre = file.getName();
            String dir1 = file.getPath();
            reader=new FileReader(file);
            int i=1;
            while(i!=-1)
            {
                i=reader.read();
                char ch=(char) i;
                buffer.append(ch);
            }
            buffer.deleteCharAt(buffer.length()-1);
            JTextPane textPane = new JTextPane();
            textPane.setText(buffer.toString());
            JScrollPane scrollPane = new JScrollPane(textPane);
            TextLineNumber tln = new TextLineNumber(textPane);
            scrollPane.setRowHeaderView( tln );
            tabs.addTab(nombre,scrollPane,dir1);
            tabs.setSelectedIndex(tabs.getTabCount()-1);
          
    
            
        }catch (FileNotFoundException ex) {
        }catch (IOException ex) {
        }
        }
    }//GEN-LAST:event_AbrirActionPerformed

    private void GuardarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarCActionPerformed
        // TODO add your handling code here:
     
        try
        {
            JTabbedPaneCloseButton.CloseButtonTab as = (JTabbedPaneCloseButton.CloseButtonTab) tabs.getTabComponentAt(tabs.getSelectedIndex()).getComponentAt(0, 0);
            JScrollPane ax = (JScrollPane) as.tab;
            JTextPane text = (JTextPane) ax.getViewport().getComponent(0);
      
            JFileChooser file=new JFileChooser();
            file.showSaveDialog(this);
            File guarda =file.getSelectedFile();
            String nombre=guarda.getName();
  if(guarda !=null)
  {
    FileWriter  save = new FileWriter(guarda);
    save.write(text.getText());
    save.close();
    as.jLabel.setText(nombre);
    as.dir = guarda.getPath();
    tabs.setTabComponentAt(tabs.getSelectedIndex(), as);
    JOptionPane.showMessageDialog(null,
         "El archivo se a guardado Exitosamente",
             "Información",JOptionPane.INFORMATION_MESSAGE);
    }
 }catch(IOException ex){
     System.out.println("error");
  }
    }//GEN-LAST:event_GuardarCActionPerformed

